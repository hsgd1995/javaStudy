package com.entor.operator;

public class OperatorTest {

	public static void main(String[] args) {

		// 两个小数的四则运算
		double a = 1;
		double b = 2;

		// 字符串+任何=字符串任何（此处的+是拼接）
		System.out.println("a + b = " + (a + b));// 3.0
		System.out.println("a - b = " + (a - b));// -1.0
		System.out.println("a * b = " + (a * b));// 2.0
		System.out.println("a / b = " + (a / b));// 0.5
		System.out.println("a % b = " + (a % b));// 1.0
	}

}
